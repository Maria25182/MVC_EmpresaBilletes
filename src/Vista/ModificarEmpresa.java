    private void cmbPaisEmpresaNuevoFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cmbPaisEmpresaNuevoFocusGained
        cmbPaisEmpresaNuevo.removeAllItems();
        int i = 0;
        while (i < MenuAdmin.Arrpais.getPosAct()) {
            cmbPaisEmpresaNuevo.addItem(MenuAdmin.Arrpais.getArrPais()[i].getPais());
            i++;

        }
    }//GEN-LAST:event_cmbPaisEmpresaNuevoFocusGained

    private void cmbIDEmpresaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbIDEmpresaItemStateChanged
        String elemento = cmbIDEmpresa.getItemAt(cmbIDEmpresa.getSelectedIndex());
        if (elemento.indexOf("Seleccione") != -1) {
            return;
        }

        //cargar los datos del formulario
        Empresa empresas = MenuAdmin.ArrEmpresa.busModEmpresa(Integer.parseInt(elemento));
        txtNombreEmpresaNuevo.setText("" + empresas.getNombreEmpresa());        // TODO add your handling code here:
    }//GEN-LAST:event_cmbIDEmpresaItemStateChanged

    /**
     * @param args the command line arguments
     */
    private void cargarCodigoEmpresa() {
        cmbIDEmpresa.removeAllItems();
        int i = 0;
        cmbIDEmpresa.addItem("Seleccione un codigo de la lista");
        while (i < MenuAdmin.ArrEmpresa.getPosAct()) {
            cmbIDEmpresa.addItem("" + MenuAdmin.ArrEmpresa.getArrEmp()[i].getIDEmpresa());
            i++;
        }
    }

