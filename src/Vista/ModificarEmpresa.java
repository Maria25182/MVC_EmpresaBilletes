    private void ModificarEmpresabtbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarEmpresabtbActionPerformed
        int indiceEmpresa = MenuAdmin.ArrEmpresa.getIndexEmp(
                Integer.parseInt(cmbIDEmpresa.getItemAt(cmbIDEmpresa.getSelectedIndex())));

        Empresa Emp = MenuAdmin.ArrEmpresa.getArrEmp()[indiceEmpresa];
        //modificar atributos por los nuevos

        //capturar valores y hacer las validaciones
        String nombree = txtNombreEmpresaNuevo.getText();
        Pais pais = new Pais();
        int IndicePais = cmbPaisEmpresaNuevo.getSelectedIndex();
        String Pais = cmbPaisEmpresaNuevo.getItemAt(IndicePais);
        pais.setPais(Pais);

        //asignar nuevos valores al objeto
        Emp.setNombreEmpresa(nombree);
        Emp.setPaisEmpresa(pais);

        //modifico el array para que quede editado
        MenuAdmin.ArrEmpresa.getArrEmp()[indiceEmpresa] = Emp;
        JOptionPane.showMessageDialog(null, "Modificacion Exitosa");


    }//GEN-LAST:event_ModificarEmpresabtbActionPerformed

