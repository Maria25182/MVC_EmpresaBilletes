    private void cbCodigoNuevoMatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cbCodigoNuevoMatMouseClicked
    String elemento = cbCodigoNuevoMat.getItemAt(cbCodigoNuevoMat.getSelectedIndex());
        if (elemento.indexOf("Seleccione")!= -1) {
            return;
        }

        //cargar los datos del formulario
        Materiales materiales = MenuAdmin.Arrmateriales.buscarmaterial(Integer.parseInt(elemento));
  
        txtDescrNuevo.setText("" + materiales.getDescripcion());
//       cmTipoMatNuevo.getItemAt(Integer.parseInt(materiales.getNombreMaterial()));   
        cmTipoMatNuevo.setSelectedItem(materiales.getNombreMaterial());           // TODO add your handling code here:
    }//GEN-LAST:event_cbCodigoNuevoMatMouseClicked

    private void cargamat() {
     cbCodigoNuevoMat.removeAllItems();
        int i = 0;
        cbCodigoNuevoMat.addItem("Seleccione un codigo de la lista");
        while (i < MenuAdmin.Arrmateriales.getPosAct()) {
            cbCodigoNuevoMat.addItem("" + MenuAdmin.Arrmateriales.getArrMateriales()[i].getID());
            i++;
        }
    }

