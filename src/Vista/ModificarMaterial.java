    private void BTBModificarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTBModificarMaterialActionPerformed
        int indiceMaterial = MenuAdmin.Arrmateriales.getIndexMat(
                Integer.parseInt(cbCodigoNuevoMat.getItemAt(cbCodigoNuevoMat.getSelectedIndex())));

        Materiales materialEdit = MenuAdmin.Arrmateriales.getArrMateriales()[indiceMaterial];
        //modificar atributos por los nuevos

        //capturar valores y hacer las validaciones
        String descr = txtDescrNuevo.getText();
       
      

        //asignar nuevos valores al objeto
        materialEdit.setDescripcion(descr);
        materialEdit.setNombreMaterial(this.cmTipoMatNuevo.getSelectedItem().toString());
       

        //modifico el array para que quede editado
        MenuAdmin.Arrmateriales.getArrMateriales()[indiceMaterial] = materialEdit;
        JOptionPane.showMessageDialog(null, "modificacion exitosa");
    }//GEN-LAST:event_BTBModificarMaterialActionPerformed

